export type MessageKey = keyof (typeof messages)["en"];

export function isSupportedLocale(locale?: string | null): locale is Locale {
  if (!locale) return false;
  return (SUPPORTED_LOCALES as readonly string[]).includes(locale);
}

export function getLocaleFromString(locale?: string | null): Locale {
  if (isSupportedLocale(locale)) return locale;
  return DEFAULT_LOCALE;
}

export function formatMessage(
  template: string,
  values?: Record<string, string | number>
) {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (match, key) =>
    Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : match
  );
}

export function createTranslator(locale: Locale) {
  return function t(key: MessageKey, values?: Record<string, string | number>) {
    const table = messages[locale] ?? messages[DEFAULT_LOCALE];
    const template = table[key] ?? messages[DEFAULT_LOCALE][key] ?? key;
    return formatMessage(template, values);
  };
}

const displayNameCache = new Map<string, Intl.DisplayNames>();

export function getLanguageLabel(locale: Locale, language: Locale) {
  const cacheKey = locale;
  let display = displayNameCache.get(cacheKey);
  if (!display) {
    display = new Intl.DisplayNames(locale, { type: "language" });
    displayNameCache.set(cacheKey, display);
  }
  return display.of(language) ?? language;
}

